#include "hashtable.h"

void	ft_putstr(char *s)
{
	if (!s)
		return ;
	while (*s)
	{
		write(1, s, 1);
		s++;
	}
}

void	ft_putstr_without_newline(char *s)
{
	if (!s)
		return ;
	while (*s)
	{
		if (*s == '\n')
			break ;
		write(1, s, 1);
		s++;
	}
}
